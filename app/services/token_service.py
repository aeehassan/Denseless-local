class InsufficientTokensError(Exception):
    """
    Raised when a student's token balance has reached zero.

    Caught in the router layer and returned as a 402 HTTP response
    with a user-facing message. Never surfaces as a raw 500 error.

    Example:
        try:
            result = run_qa_chain(student_id=user["id"], ...)
        except InsufficientTokensError:
            return templates.TemplateResponse("errors/tokens_exhausted.html", ...)
    """
    pass



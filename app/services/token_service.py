# Root directory for all learner profile JSON files
_PROFILES_DIR = Path(__file__).parent.parent.parent / "data" / "profiles"

# Default profile template — matches the agreed learner data structure
_DEFAULT_PROFILE = {
    "user_id": "",
    "learning_pace": "average",
    "tokens_remaining": 1_000_000,
    "tokens_used": 0,
    "weak_topics": [],
    "strong_topics": [],
    "revision_dates": [],
    "topics": {},
}


def _get_profile_path(student_id: str) -> Path:
    """Returns the path to a student's JSON profile file."""
    if not student_id or not student_id.strip():
        raise ValueError("student_id cannot be empty or whitespace.")
    return _PROFILES_DIR / f"{student_id}.json"


def _load_profile(student_id: str) -> dict:
    """
    Load a student's JSON profile from disk.

    If the profile file does not exist, creates a new one from the
    default template with the student_id pre-filled. This mimics
    the 'create on signup' behaviour that Supabase will handle later.

    Args:
        student_id: Unique identifier for the student.

    Returns:
        Dict representing the student's full learner profile.

    Raises:
        RuntimeError: If the profile file exists but cannot be read
                      or contains malformed JSON.
    """
    profile_path = _get_profile_path(student_id)

    # Create the profiles directory if it doesn't exist yet
    _PROFILES_DIR.mkdir(parents=True, exist_ok=True)

    if not profile_path.exists():
        # First time this student is seen — create a fresh profile
        profile = {**_DEFAULT_PROFILE, "user_id": student_id}
        _save_profile(student_id, profile)
        print(f"  [token_service] New profile created for student: {student_id}")

    try:
        with open(profile_path, "r", encoding="utf-8") as f:
            return json.load(f)
    except json.JSONDecodeError as e:
        raise RuntimeError(
            f"Profile file for student '{student_id}' exists but contains "
            f"malformed JSON. File: {profile_path} | Error: {e}"
        ) from e
    except OSError as e:
        raise RuntimeError(
            f"Could not read profile file for student '{student_id}'. "
            f"File: {profile_path} | Error: {e}"
        ) from e


def _save_profile(student_id: str, profile: dict) -> None:
    """
    Persist a student's updated profile dict back to disk as JSON.

    Args:
        student_id: Unique identifier for the student.
        profile:    The full updated profile dict to save.

    Raises:
        RuntimeError: If the file cannot be written due to permission
                      or disk space issues.
    """
    profile_path = _get_profile_path(student_id)
    _PROFILES_DIR.mkdir(parents=True, exist_ok=True)

    try:
        with open(profile_path, "w", encoding="utf-8") as f:
            json.dump(profile, f, indent=2)
    except OSError as e:
        raise RuntimeError(
            f"Could not write profile file for student '{student_id}'. "
            f"File: {profile_path} | Error: {e}"
        ) from e


